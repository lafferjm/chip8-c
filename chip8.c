void load_font(CPU* cpu) {
    const uint8_t FONT_SET[80] = {
        0xF0, 0x90, 0x90, 0x90, 0xF0, // 0
        0x20, 0x60, 0x20, 0x20, 0x70, // 1
        0xF0, 0x10, 0xF0, 0x80, 0xF0, // 2
        0xF0, 0x10, 0xF0, 0x10, 0xF0, // 3
        0x90, 0x90, 0xF0, 0x10, 0x10, // 4
        0xF0, 0x80, 0xF0, 0x10, 0xF0, // 5
        0xF0, 0x80, 0xF0, 0x90, 0xF0, // 6
        0xF0, 0x10, 0x20, 0x40, 0x40, // 7
        0xF0, 0x90, 0xF0, 0x90, 0xF0, // 8
        0xF0, 0x90, 0xF0, 0x10, 0xF0, // 9
        0xF0, 0x90, 0xF0, 0x90, 0x90, // A
        0xE0, 0x90, 0xE0, 0x90, 0xE0, // B
        0xF0, 0x80, 0x80, 0x80, 0xF0, // C
        0xE0, 0x90, 0x90, 0x90, 0xE0, // D
        0xF0, 0x80, 0xF0, 0x80, 0xF0, // E
        0xF0, 0x80, 0xF0, 0x80, 0x80  // F
    };

    for (int i = 0; i < 80; i++) {
        cpu->memory[i] = FONT_SET[i];
    }
}

void load_rom(char* rom_name, CPU* cpu) {
    FILE *fp = fopen(rom_name, "rb");
    
    if (fp == NULL) {
        printf("Error opening rom %s\n", rom_name);
        exit(-1);
    }

    long rom_length;
    uint8_t *rom_buffer;

    fseek(fp, 0, SEEK_END);
    rom_length = ftell(fp);
    fseek(fp, 0, SEEK_SET);

    rom_buffer = malloc(sizeof(uint8_t) * rom_length);
    fread(rom_buffer, sizeof(uint8_t), rom_length, fp);

    for (int i = 0; i < rom_length; i++) {
        cpu->memory[i + 0x200] = rom_buffer[i];
    }

    fclose(fp);
    free(rom_buffer);
}